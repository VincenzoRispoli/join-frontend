class Contact {
    user
    first_name
    last_name
    email
    phone
    badge_color

    constructor(user, first_name, last_name, email, phone, badge_color) {
        this.user = user
        this.first_name = first_name
        this.last_name = last_name
        this.email = email
        this.phone = phone
        this.badge_color = badge_color
    }
}